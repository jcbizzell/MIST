   public void updateSettings(RunSettings run){
       //restBox.setEnabled((run.getRestEnabled() == 1));
       //controlBox.setEnabled((run.getControlEnabled() == 1));
       //expBox.setEnabled((run.getExperimentalEnabled() == 1));
       restBox.setSelected((run.getRestEnabled() == 1));
       controlBox.setSelected((run.getControlEnabled() == 1));
       expBox.setSelected((run.getExperimentalEnabled() == 1));
       restAcqBox.setText(Integer.toString(run.getRestAcquisitions()));
       ctrlAcqBox.setText(Integer.toString(run.getControlAcquisitions()));
       expAcqBox.setText(Integer.toString(run.getExperimentalAcquisitions()));
       speedSlider.setValue(run.getControlConditionSpeed());
       subjIDBox.setText(run.getSubjID());
       outFileBox.setText(run.getFilePath());
       outFileNameBox.setText(run.getFileName());
       scanKeyBox.setText(run.getScannerKey());
       leftResponseBox.setText(run.getLeftKey());
       rightResponseBox.setText(run.getRightKey());
       confirmResponseBox.setText(run.getConfirmKey());
       ctrlTimeoutBox.setEnabled((run.getControlTimeout() == 1));
       nBackBox.setText(Integer.toString(run.getNBackNum()));
       corrFeedBack.setText(run.getCorrectFeedback());
       inCorrFeedBack.setText(run.getIncorrectFeedback());
       recFeedBack.setText(run.getRecordedFeedback());
       notRecFeedBack.setText(run.getNotRecordedFeedback());
       enableDISDAQBox.setSelected((run.getCountDISDAQs() == 1));
       DISDAQAcquisitionsField.setText(Integer.toString(run.getDISDAQtime()));
       enableSoundBox.setSelected((run.getSoundEnabled() == 1));
       childVcheckBox.setSelected((run.getChildrensVersion() == 1));
       condDiffSlider.setValue(run.getConditionDifficulty());
    
        if (run.getOrderOfConditions() == 0){
                strictButton.setSelected(true);
                randomButton.setSelected(false);
        }
        else if (run.getOrderOfConditions() == 1){
                randomButton.setSelected(true);
                strictButton.setSelected(false);
           
        }
        if (run.getRepetitionsWithin()==1) {
            oneButton.setSelected(true);
            twoButton.setSelected(false);
            threeButton.setSelected(false);
        }
        else if (run.getRepetitionsWithin()==2){
            oneButton.setSelected(false);
            twoButton.setSelected(true);
            threeButton.setSelected(false);
        }
        else if (run.getRepetitionsWithin()==3){
            oneButton.setSelected(false);
            twoButton.setSelected(false);
            threeButton.setSelected(true);
        }
      
       if (run.getScannerSignal() == 0){
           
           mouseButton.setSelected(true);
           kbdButton.setSelected(false);
           noneButton.setSelected(false);
       }
       else if (run.getScannerSignal() == 1){
           mouseButton.setSelected(false);
           kbdButton.setSelected(true);
           noneButton.setSelected(false);
       }
       else if (run.getScannerSignal() == 2){
           mouseButton.setSelected(false);
           kbdButton.setSelected(false);
           noneButton.setSelected(true);
       }
   } 
   
