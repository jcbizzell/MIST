    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        // TODO add your handling code here:
        int order = 1;
        int repetitions = 3;
        int signal = 2;
        if (strictButton.isSelected()) {
            order = 0;
        }
        else {
            order = 1;
        }
        if (oneButton.isSelected()) {
            repetitions = 1; 
        }
        else if (twoButton.isSelected()){
            repetitions = 2;
        }
        else {
            repetitions = 3;
        }
       if (mouseButton.isSelected()){
           signal = 0;
       }
       else if (kbdButton.isSelected()){
           signal = 1;
       }
       else {
           signal = 2;
       }
       RunSettings run1 = new RunSettings(restBox.isSelected(), controlBox.isSelected(), expBox.isSelected(), restAcqBox.getText(), ctrlAcqBox.getText(), expAcqBox.getText(), speedSlider.getValue(), order, repetitions, subjIDBox.getText(), outFileBox.getText(), outFileNameBox.getText(), signal, scanKeyBox.getText(), leftResponseBox.getText(), rightResponseBox.getText(), confirmResponseBox.getText(), ctrlTimeoutBox.isSelected(), nBackBox.getText(), corrFeedBack.getText(), inCorrFeedBack.getText(), recFeedBack.getText(), notRecFeedBack.getText(), enableDISDAQBox.isSelected(), DISDAQAcquisitionsField.getText(), enableSoundBox.isSelected(), childVcheckBox.isSelected(), condDiffSlider.getValue());
       
       String path = run1.getFilePath() + "\\" +run1.getFileName() + "settings.txt";
       File file = new File(path);
       // If file exists, append a "+" to the file name
       while (file.exists()) {
           path = run1.getFilePath() + "\\" +run1.getFileName() + "+settings.txt";
           run1.setFileName(run1.getFileName()+"+");
           file = new File(path);
        }
       
       file.getParentFile().mkdirs();
       String output = run1.write();
       
       try{
        PrintWriter out = new PrintWriter(file);
        out.println(output);
        //System.out.println(output);
        out.close();
       }
       catch(FileNotFoundException fnfe) {
            JOptionPane.showMessageDialog(null, "Please Select a valid file path.");
       }
       
      startTask(run1);
      
    }//GEN-LAST:event_startButtonActionPerformed
